package com.mycompany.days29_tipedatafloat;


public class Days29_TipeDataFloat {

    public static void main(String[] args) {
    
        float angkaA = 12.8f;
        float angkaB = 7.90f;
        float hasilPenjumlahan = angkaA + angkaB;        
        float hasilPengurangan = angkaA - angkaB;
        float hasilPerkalian = angkaA * angkaB;        
        float hasilPembagian = angkaA / angkaB;

        System.out.println("Hasil Penjumlahan: " + hasilPenjumlahan);
        System.out.println("Hasil Pengurangan: " + hasilPengurangan);
        System.out.println("Hasil Perkalian: " + hasilPerkalian);
        System.out.println("Hasil Pembagian: " + hasilPembagian);
    }
}

    

