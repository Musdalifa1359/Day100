
package com.mycompany.days40_perulangandengentipedataint;

public class Days40_PerulanganDengenTipeDataint {

    public static void main(String[] args) {
        for (int A = 1; A <= 10; A++) {
            System.out.println("Nilai A: " + A);
        }

        int B = 1;
        while (B <= 10) {
            System.out.println("Nilai B: " + B);
            B++;
        }
        int C = 1;
        do {
            System.out.println("Nilai C: " + C);
            C++;
        } while (C <= 10);
    }
}

