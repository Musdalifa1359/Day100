
package com.mycompany.days32_operasitypedataint;

public class Days32_OperasiTypeDataInt {

    public static void main(String[] args) {

        int angkaA = 67;
        int angkaB = 13;
        int hasilPenambahan = angkaA + angkaB;
        System.out.println("Hasil Penambahan: " + hasilPenambahan);

        int hasilPengurangan = angkaA - angkaB;
        System.out.println("Hasil Pengurangan: " + hasilPengurangan);

        int hasilPerkalian = angkaA * angkaB;
        System.out.println("Hasil Perkalian: " + hasilPerkalian);

        int hasilPembagian = angkaA / angkaB;
        System.out.println("Hasil Pembagian: " + hasilPembagian);
    }
}



    

