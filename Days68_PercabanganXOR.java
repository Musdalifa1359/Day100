
package days68_percabanganxor;

public class Days68_PercabanganXor {

    public static void main(String[] args) {
       
        boolean a = true;
        boolean b = false;

        boolean c = a ^ b;

        if (c) {
            System.out.println("Nilai XOR adalah true");
        } else {
            System.out.println("Nilai XOR adalah false");
        }
    }
}

