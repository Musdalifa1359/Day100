
package com.mycompany.days28_tipedatalong;

public class Days28_TipeDatalong {

    public static void main(String[] args) {
       
        long angka1 = 340L;
        long angka2 = 23L;
        
        long hasilPenjumlahan = angka1 + angka2;
        System.out.println("Hasil Penjumlahan: " + hasilPenjumlahan);

        long hasilPengurangan = angka1 - angka2;
        System.out.println("Hasil Pengurangan: " + hasilPengurangan);

        long hasilPerkalian = angka1 * angka2;
        System.out.println("Hasil Perkalian: " + hasilPerkalian);

        long hasilPembagian = angka1 / angka2;
        System.out.println("Hasil Pembagian: " + hasilPembagian);
    }
}

    

