package com.mycompany.days37_tipedataprimitifboolean;

public class Days37_TipeDataPrimitifboolean {

    public static void main(String[] args) {

        boolean isTrue = true;
        boolean isFalse = false;
        System.out.println("isTrue: " + isTrue);
        System.out.println("isFalse: " + isFalse);
        if (isTrue) {
            System.out.println("Ini akan ditampilkan karena isTrue bernilai true.");
        } else {
            System.out.println("Ini tidak akan ditampilkan.");
        }
    }
}


