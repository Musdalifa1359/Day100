package com.mycompany.days31_tipedatadouble;

public class Days31_TipeDatadouble {

    public static void main(String[] args) {
        double nilaiDouble1 = 32.8;
        double nilaiDouble2 = 7.980;
        double hasilPenjumlahan = nilaiDouble1 + nilaiDouble2;
        double hasilPengurangan = nilaiDouble1 - nilaiDouble2;
        double hasilPerkalian = nilaiDouble1 * nilaiDouble2;
        double hasilPembagian = nilaiDouble1 / nilaiDouble2;
      
        System.out.println("Hasil Penjumlahan: " + hasilPenjumlahan);
        System.out.println("Hasil Pengurangan: " + hasilPengurangan);
        System.out.println("Hasil Perkalian: " + hasilPerkalian);
        System.out.println("Hasil Pembagian: " + hasilPembagian);
    }
}



