package com.mycompany.days29_tipedatafloat;

public class Days30_TipeDataShort {
    public static void main(String[] args) {
        short nilaiShort = 12345;

        System.out.println("Nilai short: " + nilaiShort);
        short hasilPenambahan = (short) (nilaiShort + 125);
        short hasilPengurangan = (short) (nilaiShort - 60);
        short hasilPerkalian = (short) (nilaiShort * 2);
        System.out.println("Hasil Penambahan: " + hasilPenambahan);
        System.out.println("Hasil Pengurangan: " + hasilPengurangan);
        System.out.println("Hasil Perkalian: " + hasilPerkalian);
    }
}
  

